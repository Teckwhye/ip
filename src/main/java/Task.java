public class Task {

    /* Name of task */
    private String name;

    /* Constructer of Task */
    public Task(String name) {
        setName(name);
    }

    /* Getter for task name */
    public String getName() {
        return name;
    }

    /* Setter for task name */
    public void setName(String name) {
        this.name = name;
    }
}
